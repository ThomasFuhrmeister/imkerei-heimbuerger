        <h1 class="h1">
            Wie kommt der Honig ins Glas?
        </h1>

        <h3 class="h3">
            Honiggewinnung
        </h3>
        <p>
            Bevor der Honig auf den Tisch gelangt, müssen Bienen und Imker viel leisten.
            Grob beschrieben sammelt die Biene den Nektar der Blüten, dickt den Honig ein und verdeckelt die Waben. Der Imker nimmt anschließend die reifen Waben aus den Beuten heraus, schleudert den Honig und füllt ihn in Gläser ab.

        </p>

        <h3 class="h3">
            Die Arbeit der Biene
        </h3>
        <p>
            Geruch und Aussehen der Blüten locken die Bienen an. Aus den Nektarien der Blüten saugen die Bienen den Nektar, das Ausgangsprodukt des Honigs. Für ein Kilogramm Honig müssen Bienen drei Kilogramm Nektar sammeln. Dabei legen die Bienen eine Strecke entsprechend einer Erdumrundung zurück. 

            Der eingetragene Nektar besteht bis zu 60 % aus Wasser. Würde der Honig mit solch hohem Wassergehalt gelagert werden, bestünde die Gefahr der Gärung. Da Honig für die Bienen ein Wintervorrat ist, muss er haltbar sein. Durch Wasserentzug (Verdunstung) und den Zusatz körpereigener Enzyme (Fermente) machen die Bienen den Honig haltbar. Ist er reif, wird er in Zellen deponiert und anschließend mit einem Wachsdeckel verschlossen, um nachträgliche Feuchtigkeit zu verhindern. Der Wassergehalt des Honigs beträgt nun nur noch 18 %. 

            Je größer das Bienenvolk, desto schneller und besser können große Nektarmengen für den Winter aufbereitet werden. Da der Imker den Bienen die Vorräte für den Winter nimmt, muss er sie als Ersatz z. B. mit Zuckersirup füttern. Dieses Ersatzfutter arbeiten die Bienen wie Nektar um.
        </p>

        <h3 class="h3">
            die Arbeit der Imker
        </h3>
        <p>
            Sind mindestens zwei Drittel der Waben verdeckelt, dann ist der Honig reif und kann vom Imker geerntet werden. Zusätzlich wird der Wassergehalt nochmal kontrolliert um ein qualitativ einwandfreies Produkt zu gewährleisten. Der Imker entnimmt die mit Honig gefüllten Waben. Für die Einschüchterung der Bienen während der Ernte werden Rauch, Luftströme, und Kehrbesen verwendet.

            Im Schleuderraum der Imkerei werden die Zelldeckel der Waben mit der Endeckelungsgabel entfernt. Mit Hilfe der Honigschleuder wird der Honig anschließend aus den Waben geschleudert und durch ein Sieb in einen 25Liter Honigeimer gefiltert. Wenige Tage nach der Ernte werden die abgesetzten Luftbläschen und Wachspartikel an der Oberfläche des Eimers abgeschöpft und der fertige Honig regelmäßig gerührt, bis er glänzt und eine perlmuttartige Konsistenz erhält. Anschließend wird er in Gläser gefüllt, etikettiert und kann zum Verkauf angeboten werden.
            Die optimale Lagerbedingung für den Honig sind ca. 15°C in einem trockenen, dunklen Raum.

        </p>






